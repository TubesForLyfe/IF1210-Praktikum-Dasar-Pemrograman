module LuasLingkaran where

--DEFINISI DAN SPESIFIKASI
luasLingkaran :: Float -> Float
{-  luasLingkaran yang menerima masukan sebuah bilangan real (float) r 
yang merepresentasikan jari-jari sebuah lingkaran (asumsikan  r > 0) dan 
menghasilkan luas lingkaran berdasarkan rumus: luas = 3.1415 * r * r.-}

--REALISASI
luasLingkaran r = 3.1415 * r * r